#                         file = file.path(PSYTOOLS_DATA_DICTIONARY_DIR, paste0(name, '.Rmd')),
#                         render = FALSE,
#                         openResult = FALSE,
#                         codebook = TRUE,
#                         replace = TRUE)

          # # Stata requires no dots in the names and missings are represented differently
