                col_names = FALSE, .name_repair = make.names))
            d <- labelData(d, resources)
            write_sav(d, gsub("csv", "zsav", filepath), compress = TRUE)
            # This generates an RmdCodebook - could be set to render it to html if you prefer
            # Note the autogenerated labelling during derivations will be lost if the codebook saved at this stage
            # Commented out to avoid overwriting the codebooks generated during derivation.
            # We could modify the resources document to add the derivitive codes but it seems to be less work and more
            #   easily extendable to have the anonimisation script able to work off the labelled zsavs
